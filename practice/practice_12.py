def triangles():
    triangles_yang = [1]
    yield triangles_yang

    while True:
        temp = [1]
        for i in range(len(triangles_yang) - 1):
            temp.append(triangles_yang[i] + triangles_yang[i + 1])
        temp.append(1)
        triangles_yang = temp
        yield triangles_yang

n = 0
results = []
for t in triangles():
    print(t)
    results.append(t)
    n = n + 1
    if n == 10:
        break
